static int	al(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}

static int	num(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int			ft_isalnum(int c)
{
	if (al(c) == 1 || num(c) == 1)
		return (1);
	return (0);
}
